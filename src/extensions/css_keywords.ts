/** All implemented css keywords */
type BaseCSSColors = keyof typeof baseCssColors;
export type CssKeywords =
  | BaseCSSColors
  | `bg${Capitalize<BaseCSSColors>}`;

for (const [name, code] of Object.entries(baseCssColors)) {
  extendStyle(name, code(false));
  const bgName = `bg${name[0].toUpperCase()}${name.slice(1)}`;
  extendStyle(bgName, code(true));
}

type CSSStyle = { [style in CssKeywords]: Crayon };
declare global {
  // deno-lint-ignore no-empty-interface
  interface Crayon extends CSSStyle {}
}
